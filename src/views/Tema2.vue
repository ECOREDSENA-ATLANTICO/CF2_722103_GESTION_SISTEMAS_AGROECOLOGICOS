        p.mb-3 El concepto multifuncional en el entorno agrícola se usa para indicar el carácter heterogéneo y complejo de la agricultura en el que refiere que los diversos elementos que componen el agroecosistema no solo están próximos, sino que interactúan constantemente. Esta visión ha permitido entender y describir la naturaleza compleja de los agroecosistemas y su interacción continua con los ecosistemas naturales. Transformando la visión reduccionista de la agricultura.
        p.mb-3 Del mismo modo, la agroecología es multidimensional ya que la estrecha integración de la agricultura y el territorio muestra como el intercambio de esta con otras dimensiones sociales, económicas, políticas, culturales y productivas logran un equilibrio ecológico, político, social, económico y de los servicios ecosistémicos. Mostrando que los agroecosistemas son un sistema abierto y permeable al contexto del territorio.
        p.mb-0 Sin embargo, la transición de agroecología no solo se basa en estos enfoques, sino que requiere de otros enfoques que en conjunto representan un cambio o transformación de la agricultura moderna hacia una agricultura más sostenible, los cuales son:

